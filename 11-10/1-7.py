p = float(input())
if p <= 100000:
    l = 0.1 * p
if 100000 < p <= 200000:
    l = 0.1 * 100000 + 0.075 * (p - 100000)
if 200000 < p <= 400000:
    l = 0.1 * 100000 + 0.075 * 100000 + 0.05 * (p - 200000)
if 400000 < p <= 600000:
    l = 0.1 * 100000 + 0.075 * 100000 + 0.05 * 200000 + 0.03 * (p - 400000)
if 600000 < p <= 1000000:
    l = (
        0.1 * 100000
        + 0.075 * 100000
        + 0.05 * 200000
        + 0.03 * 200000
        + 0.015 * (p - 600000)
    )
if p > 1000000:
    l = (
        0.1 * 100000
        + 0.075 * 100000
        + 0.05 * 200000
        + 0.03 * 200000
        + 0.015 * 400000
        + 0.01 * (p - 1000000)
    )
print("%.2f" % l)
